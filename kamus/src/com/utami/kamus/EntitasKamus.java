package com.utami.kamus;

public class EntitasKamus {
	String istilah = "";
	String arti = "";

	public void setIndo(String in) {
		this.istilah = in;
	}

	public String getIndo() {
		return istilah;
	}

	public void setJawa(String j) {
		this.arti = j;
	}

	public String getJawa() {
		return arti;
	}
}
